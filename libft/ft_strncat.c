#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int c;

	i = 0;
	c = ft_strlen(s1);
	while (n-- && s2[i])
	{
		s1[c++] = s2[i];
		i++;
	}
	s1[c++] = '\0';
	return (s1);
}
