#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	char	*tmp;

	i = 0;
	if (!(tmp = (char *)malloc(ft_strlen(s1) + 1)))
		return (NULL);
	while (s1[i] != '\0')
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
